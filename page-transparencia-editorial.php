													<p><strong>Dictaminación por pares</strong></p>

													<p><em>Comunicación Científica</em> dispone de un sistema de arbitraje de doble ciego para sus publicaciones. Los evaluadores son investigadores nacionales y extranjeros de reconocido prestigio en sus áreas de especialización. La dictaminación se centra en la organización, coherencia, cohesión, aspectos cualitativos y aspectos cuantitativos del contenido del texto. El proceso de dictaminación tiene las siguientes fases:</p>

													<p>Fase 1.</p>

													<p>El artículo o texto sometido a evaluación debe ser estrictamente original. Éste debe ser enviado al Comité dictaminador para que dé inició la distribución a especialistas en la materia. Será función exclusiva del Comité editorial determinar a quién se envía a dictaminar un texto. El proceso de dictaminación será anónimo, tanto para los dictaminadores como para los autores</p>

													<p>Los dictámenes deberán entregarse en un plazo máximo de 8 semanas y deberán argumentar el valor del trabajo, en términos de su contenido, rigor conceptual, aportaciones al campo del conocimiento y adecuación formal (es decir, exposición clara, organización coherente y uso adecuado de la lengua escrita).</p>

													<p>Fase 2.</p>

													<p>Las dictaminaciones pueden aceptar los textos sin cambios o aceptarlo con sugerencias (publicable), condicionarlo a cambios mayores (publicable con modificaciones) o rechazarlo (no publicable), así como solicitar que se consulte al autor sobre cualquier aspecto de forma o contenido que resulte relevante.</p>

													<p>Una contribución será aceptada para su publicación si recibe dos dictámenes favorables. Si el trabajo es condicionado a realizar cambios mayores, la nueva versión recibe una segunda lectura por parte de uno de los dictaminadores, y el autor debe enviar una carta explicando las modificaciones que ha realizado, antes de recibir una respuesta definitiva.</p>

													<p>En caso de un resultado No publicable, el texto debe ser evaluado por un tercer dictaminador para confirmar el resultado.</p>

													<p>Fase 3.</p>

													<p>El Comité dictaminador enviará a sus autores y/o coordinadores los resultados de las dictaminaciones y se confirmará por escrito que el proceso de dictaminación se cumplió a cabalidad. La transparencia de todo el proceso de dictaminación se publicará en nuestra página oficial.</p>


